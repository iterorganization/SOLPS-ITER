hold all;
semilogy(y2(1:ny+2,npl+2),na03(1:ny+2,npl+2),'-*','LineWidth',2,'Color',[0.0,0,0]);
hold all;
semilogy(y2(1:ny+2,npl+2),na04(1:ny+2,npl+2),'-+','LineWidth',2,'Color',[0,0,1]);

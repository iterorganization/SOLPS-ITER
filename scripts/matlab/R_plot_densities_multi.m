ylabel('n,  m^{-3}','FontName','Times','FontSize',30);
title(Gtitle,'FontName','Times','FontSize',30);
legend('n_e','n_{D^0}','n_{D^{+1}}','n_{C^{0}}','n_{C^{+1}}','n_{C^{+2}}','n_{C^{+3}}','n_{C^{+4}}','n_{C^{+5}}','n_{C^{+6}}','n_{He^{0}}','n_{He^{+1}}','n_{He^{+2}}','interpreter','latex','Location','NorthEastOutside');
